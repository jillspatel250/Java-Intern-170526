public class Literals {
    public static void main(String[] args) {
        int dec = 10;         // Decimal literal
        int bin = 0b1010;     // Binary literal
        int hex = 0xA;        // Hexadecimal literal
        float pi = 3.14f;     // Floating point literal
        char ch = 'J';        // Character literal
        String name = "Jills"; // String literal
        boolean flag = true;  // Boolean literal

        System.out.println("Decimal: " + dec);
        System.out.println("Binary: " + bin);
        System.out.println("Hexadecimal: " + hex);
        System.out.println("Float: " + pi);
        System.out.println("Char: " + ch);
        System.out.println("String: " + name);
        System.out.println("Boolean: " + flag);
    }
}
